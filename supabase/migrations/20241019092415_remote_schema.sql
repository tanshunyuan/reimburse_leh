    "id" uuid not null default gen_random_uuid(),
    "created_at" timestamp with time zone not null default now(),
    "updated_at" timestamp with time zone default now(),
    "exp_date" timestamp without time zone,
    "from" text,
    "to" text,
    "amount" numeric,
    "status" text,
